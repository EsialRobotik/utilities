package rpLidar.utils;

public class ExportValue {

	public double x;
	public double y;
	public int quality;
	public double distance;
	public double angle;

	public ExportValue(double x, double y, int quality, double distance, double angle) {
		this.x = x;
		this.y = y;
		this.quality = quality;
		this.distance = distance;
		this.angle = angle;
	}	
	
}
